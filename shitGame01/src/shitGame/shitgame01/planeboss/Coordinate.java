package shitGame.shitgame01.planeboss;

public class Coordinate {
	public float x;
	public float y;
	public Coordinate(float x,float y){
		this.x = x;
		this.y = y;
	}
	public float getX(){
		return x;
	}
	public void setX(float x){
		this.x = x;
	}
	public void setY(float y){
		this.y = y;
	}
	public float getY(){
		return y;
	}
}
